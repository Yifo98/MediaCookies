export const copyCatalog: Record<Language, Record<CopyKey, string>> = {
  'zh-CN': zhCN,
  en,
}

export function translate(
  language: Language,
  key: CopyKey,
  values: Record<string, string | number> = {},
): string {
  return copyCatalog[language][key].replace(/\{(\w+)\}/g, (_, name: string) => String(values[name] ?? `{${name}}`))
}

export function applyStaticCopy(root: ParentNode, language: Language): void {
  root.querySelectorAll<HTMLElement>('[data-copy]').forEach((element) => {
    const key = element.dataset.copy as CopyKey | undefined
    if (key) element.textContent = translate(language, key)
  })
}
